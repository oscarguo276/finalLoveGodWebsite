package com.loveGod.demo.model;

public class PoemDto{

	
	private String poemName;
	private String poemMean;
	
	public PoemDto() {
		
	}

	public String getPoemName() {
		return poemName;
	}

	public void setPoemName(String poemName) {
		this.poemName = poemName;
	}

	public String getPoemMean() {
		return poemMean;
	}

	public void setPoemMean(String poemMean) {
		this.poemMean = poemMean;
	}

	
}
